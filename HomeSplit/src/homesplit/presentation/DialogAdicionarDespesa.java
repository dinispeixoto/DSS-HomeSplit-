    /**
     * Adiciona a despesa á aplicação.
     */
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // ADICIONAR DESPESA
        try{
            String tipo_despesa = TipoChoice.getSelectedItem();
            double valor = Double.parseDouble(ValorTextField.getText());
            String descricao = DescricaoTextField.getText();
            homesplit.adicionarDespesa(tipo_despesa,descricao,valor,this.lista_moradores_incluidos);
            this.dispose();
            parent.updateFrame();
        }
        catch(Exception e){
            DialogExceptions exception = new DialogExceptions(null,true,"Insira um valor de despesa válido!");
            exception.setVisible(true);
        }
    }//GEN-LAST:event_jButton3ActionPerformed
    /**
     * Adiciona um morador á lista de moradores da despesa.
     */
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // ADICIONAR MORADOR À DESPESA
        if(UsersList.getSelectedValue() != null){
            String morador = UsersList.getSelectedValue().toString();
            morador = morador.substring(morador.indexOf("(") + 1);
            morador = morador.substring(0, morador.indexOf(")"));

            DialogAdicionarMoradorDespesa adicionar_morador = 
                    new DialogAdicionarMoradorDespesa(this.parent,this,true,homesplit,morador);
            adicionar_morador.setVisible(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    /**
     * Remove um morador da lista de moradores da despesa.
     */
    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // REMOVER MORADOR DA DESPESA
        if(UsersDespesaList.getSelectedValue() != null){
            String morador = UsersDespesaList.getSelectedValue().toString();
            morador = morador.substring(morador.indexOf(" ") + 2);
            morador = morador.substring(0, morador.indexOf(" "));
            this.lista_moradores_incluidos.remove(morador);
            updateFrame();
        }
    }//GEN-LAST:event_jButton4ActionPerformed


