    public void updateFrame(){
        updateUsersList();
        updateUsersDespesaList();
    }
    
    /**
     * Actualiza a lista de utilizadores.
     */
    public void updateUsersList(){
        DefaultListModel<String> lista = new DefaultListModel<>();
        Map<String,Morador> moradores = homesplit.getGestao_moradores().getListaMoradores();
        moradores.remove(homesplit.getUtilizador().getUsername());
        try{
            for(Map.Entry<String,Morador> entry : moradores.entrySet()){
                if(!lista_moradores_incluidos.containsKey(entry.getKey())){
                    StringBuilder nome_username = new StringBuilder();
                    nome_username.append("  ");
                    nome_username.append(entry.getValue().getNome());
                    nome_username.append("  ");
                    nome_username.append("(" + entry.getKey() + ")");
                    lista.addElement(nome_username.toString());
                }
            }
        }
        catch (Exception e){
            e.getMessage();
        }
        UsersList.setModel(lista); // ISTO FAZ APARECER UM WARNING!! 
    }
    
    /**
     * Actualiza a lista de utilizadores que farão parte da despesa.
     */
    public void updateUsersDespesaList(){
        DefaultListModel<String> lista = new DefaultListModel<>();
        try{
            for(Map.Entry<String,Double> entry : lista_moradores_incluidos.entrySet()){
                StringBuilder nome_username = new StringBuilder();
                nome_username.append("  ");
                nome_username.append(entry.getKey());
                nome_username.append("  ");
                nome_username.append("(" + entry.getValue() + ")");
                lista.addElement(nome_username.toString());
            }
        }
        catch (Exception e){
            e.getMessage();
        }
        UsersDespesaList.setModel(lista); // ISTO FAZ APARECER UM WARNING!! 
    }
    
    /**
     * Adiciona um dado morador á lista de moradores da despesa.
     */
    public void addMorador(String morador, Double percentagem){
        this.lista_moradores_incluidos.put(morador,percentagem);
        updateFrame();
    }
    
    /**
     * Verifica se uma dada percentagem é válida.
     */
    public boolean percentagemValida(double percentagem){
        double soma = 0;
        for(Double p : this.lista_moradores_incluidos.values()){
            soma += p;
        }
        if((soma + percentagem)>100 || percentagem <= 0) return false;
        return true;
    }
    
