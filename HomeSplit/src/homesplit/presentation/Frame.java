    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // PAGAR DESPESA
        if(DespesasList.getSelectedValue() != null){
            String despesa = (String)DespesasList.getSelectedValue().toString();
            despesa = despesa.substring(despesa.indexOf("(") + 1);
            despesa = despesa.substring(0, despesa.indexOf(")"));
            int id_despesa = Integer.parseInt(despesa);
            if(homesplit.prestacoesDefinidas(id_despesa)){
                DialogPagarDespesa pagar_despesa = new DialogPagarDespesa(this,true,homesplit,id_despesa);
                pagar_despesa.setVisible(true);
            }
            else{
                DialogPagarDespesaPrestacoes pagar_despesa_prestacoes = new DialogPagarDespesaPrestacoes(this,true,homesplit,id_despesa);
                pagar_despesa_prestacoes.setVisible(true);
            }
        }
    }//GEN-LAST:event_jButton8ActionPerformed
