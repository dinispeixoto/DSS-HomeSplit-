    public void updateFrame(){
        NomeLabel.setText(homesplit.getUtilizador().getNome());
        updateUsersList();
        updateDespesasResponsavelList();
        updateDespesasList();
        updateHistoricoDespesasList();
        updateNotificacoes();
        updateSaldo();
        updateAvatar();
    }
    
    /**
     * Actualiza o avatar do utilizador.
     */
    public void updateAvatar(){
        int largura, altura = 0;
        ImageIcon iconImagem;
        BufferedImage image = null;
        if(homesplit.getUtilizador().getAvatar()!=null){
            try{
                image = ImageIO.read(new File(homesplit.getUtilizador().getAvatar()));
                largura = image.getWidth();
                altura = image.getHeight();
                if(largura != LARGURA_AVATAR || altura != ALTURA_AVATAR){
                    iconImagem = new ImageIcon(image.getScaledInstance(LARGURA_AVATAR,ALTURA_AVATAR,Image.SCALE_SMOOTH));
                }
                else{
                    iconImagem = new ImageIcon(image);
                }
                avatar_principal.setIcon(iconImagem);
            }
            catch(Exception e){
                e.getMessage();
            }
        }
    }
    
