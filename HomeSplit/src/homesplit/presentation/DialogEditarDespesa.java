    /**
     * Edita uma dada despesa.
     */
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // GUARDAR ALTERAÇÕES
        Map<String,Double> lista_moradores = new HashMap<String,Double>();
        ListModel model = ListaMoradoresDespesa.getModel();

        for(int i=0; i < model.getSize(); i++){
            String info =  model.getElementAt(i).toString();
            info = info.substring(info.indexOf("(") + 1);
            info = info.substring(0, info.indexOf("%"));
            String username = model.getElementAt(i).toString();
            username = username.substring(0,username.indexOf(" "));
            lista_moradores.put(username,Double.parseDouble(info));
        }
        try{
            homesplit.editarDespesa(this.id_despesa,TipoChoice.getSelectedItem(),DescricaoDespesa.getText(),Double.parseDouble(ValorDespesa.getText()),lista_moradores);
            this.parent.updateFrame();
            this.dispose();
        }
        catch(NumberFormatException e){
            DialogExceptions exception = new DialogExceptions(null,true,"Insira um valor de despesa válido!");
            exception.setVisible(true);
        }
        catch(Exception e){
            DialogExceptions exception = new DialogExceptions(null,true,e.getMessage());
            exception.setVisible(true);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

