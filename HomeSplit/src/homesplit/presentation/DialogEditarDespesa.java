    /**
     * Actualiza a janela com os dados da despesa.
     */
    public void updateData(){
        Despesa despesa = homesplit.getDespesa(id_despesa);
        TipoChoice.select(despesa.getTipo());
        ValorDespesa.setText(Double.toString(despesa.getValor_total()));
        DescricaoDespesa.setText(despesa.getDescricao());
        DefaultListModel<String> lista = new DefaultListModel<String>();
        for(Map.Entry<String,Fracao> entry : despesa.getListaFracoes().entrySet()){
            StringBuilder info = new StringBuilder();
            double valor_despesa = homesplit.getGestao_despesas().getListaDespesas().get(this.id_despesa).getValor_total();
            double percentagem = (entry.getValue().getValor_total() / valor_despesa)* 100;
            // info.append("  ");
            info.append(entry.getKey() + "    (");
            info.append(Double.toString(percentagem));
            info.append("%)");
            lista.addElement(info.toString());
        }
        ListaMoradoresDespesa.setModel(lista);
    }
